#include <stdio.h>
#include <stdlib.h>

void main(){
    //definindo variaveis
    int a = 1, b =10;

    //contado até 10
    while(a <= 10){

        //imprimindo a
        printf("\n%d", a);

        //incremento
        a = a + 1;

    }
    //contagem regressiva
    while(b >= 1){
        //imprimindo a
        printf("\n%d", b);

        //incremento
        b = b - 1;

    }

}
