//programa conta de 1 até 10
#include <stdio.h>
#include <stdlib.h>

void main(){
    //definindo variaveis
    int a = 1;

      //contando até 10
      //primeiro entra no laço e depois compara
    do{
        //imprimindo a
        printf("\n%d", a);

        //incremento
        a ++;

    }while(a <= 10);

}

